#include "cub3d.h"

void    draw_something(t_win *win, t_img *img, int color)
{

    int x;
    int y;
    
    x = 0;
    while (x < 10)
    {
        y = 0;
        while (y < 10)
        {
            pixel_put(img, x, y, color);
            ++y;
        }
        ++x;
    }
}
